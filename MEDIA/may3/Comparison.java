package may3;

public class Comparison {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int a = 15;
        int b = 25;
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x < a);
        System.out.println(b < y);
        System.out.println(x <= y);
        System.out.println(a >= y);
        System.out.println(x != b);
        System.out.println(x == y);
        System.out.println(a == y);
        System.out.println(x == y);
    }
}
